package com.legacykeep.notification.entity;

/**
 * Notification Priority Enum
 * 
 * Defines the priority levels for notifications.
 * 
 * @author LegacyKeep Team
 * @version 1.0.0
 */
public enum NotificationPriority {
    LOW,        // Low priority notifications (marketing, newsletters)
    NORMAL,     // Standard priority notifications (regular updates)
    HIGH,       // High priority notifications (important updates)
    URGENT      // Urgent notifications (security alerts, critical updates)
}
